// Date(year, month, day, hour, minute, second, ms)
const date = new Date();
console.log(date);

// extraction
const year = date.getFullYear();
const month = date.getMonth();
const day = date.getDate();
const hour = date.getHours();
const minutes = date.getMinutes();

console.log(year);
console.log(month);
console.log(day);
console.log(hour);
console.log(minutes);
